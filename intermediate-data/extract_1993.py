def _get_bout_numbers(
    division: bracket_utils.Division, weight: int
) -> dict[bracket_utils.MatchSlot, int]:
    key = (division, weight)
    entry = _BOUT_NUMBER_INPUTS.get(key)
    if entry is None:
        return {}

    first_pigtail, first_place = entry
    if division == "novice":
        weight_count = 16
    elif division == "senior":
        weight_count = 18
    else:
        raise NotImplementedError(division)

    return {
        "championship_r32_02": first_pigtail,
        "championship_r32_04": first_pigtail + 2,
        "championship_r32_06": first_pigtail + 4,
        "championship_r32_08": first_pigtail + 6,
        "championship_r32_10": first_pigtail + 8,
        "championship_r32_12": first_pigtail + 10,
        "championship_r32_14": first_pigtail + 12,
        "championship_r32_16": first_pigtail + 14,
        "consolation_fifth_place": first_place - 2 * weight_count,
        "consolation_third_place": first_place - weight_count,
        "championship_first_place": first_place,
    }


def main():
    team_scores: dict[bracket_utils.Division, list[bracket_utils.TeamScore]] = {}
    team_scores["novice"] = []
    for team_name, score in _NOVICE_TEAM_SCORES.items():
        team_scores["novice"].append(
            bracket_utils.TeamScore(team=team_name, acronym=None, score=score)
        )

    team_scores["senior"] = []
    for team_name, score in _SENIOR_TEAM_SCORES.items():
        team_scores["senior"].append(
            bracket_utils.TeamScore(team=team_name, acronym=None, score=score)
        )

    weight_classes: list[bracket_utils.WeightClass] = []

    for weight, competitors in _NOVICE_COMPETITORS.items():
        bout_numbers = _get_bout_numbers("novice", weight)
        weight_class = bracket_utils.weight_class_from_competitors(
            "novice",
            weight,
            competitors,
            _NOVICE_TEAM_REPLACE,
            _NAME_EXCEPTIONS,
            bout_numbers,
        )
        weight_classes.append(weight_class)

    for weight, placers in _SENIOR_PLACERS.items():
        weight_class = bracket_utils.create_weight_class_from_placers(
            "senior", weight, placers, _SENIOR_TEAM_REPLACE
        )
        if weight == 130:
            _, match_3rd, _ = weight_class.matches
            match_3rd.top_competitor.full_name = "DAN DE BRUN"
            match_3rd.top_competitor.last_name = "DE BRUN"
        if weight == 138:
            _, match_3rd, _ = weight_class.matches
            match_3rd.bottom_competitor.full_name = "STANLEY EVANS JR."

        weight_classes.append(weight_class)

    extracted = bracket_utils.ExtractedTournament(
        weight_classes=weight_classes, team_scores=team_scores, deductions=[]
    )
    extracted.sort()
    with open(HERE / "extracted.1993.json", "w") as file_obj:
        file_obj.write(extracted.model_dump_json(indent=2))
        file_obj.write("\n")


if __name__ == "__main__":
    main()
